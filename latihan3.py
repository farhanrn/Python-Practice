# Program konversi suhu 

print("PROGRAM KONVERSI SUHU FARENHEIT KE SATUAN LAIN")

farenheit = float(input("Masukkan suhu dalam farenheit :"))
print("suhu adalah :", farenheit, "Farenheit")

# celcius 
celcius = 0.55*(farenheit-32)
print("suhu pada celcius adalah", celcius, "celcius")

#reamur
reamur = 0.44*(farenheit-32)
print(" suhu pada reamur adalah", reamur, "reamur")

