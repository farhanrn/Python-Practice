# stacks queue

tumpukan = [1,2,3,4,5,6]
print('data sekarang :',tumpukan)
# memasukkan data baru 

tumpukan.append(7)
print('data masuk:', 7)
tumpukan.append(8)
print('data masuk:', 8)
print('data sekarang :',tumpukan)

# stacking techniqe
tumpukan.pop() #menghilangkan data paling belakang\
print('data sekarang :',tumpukan)

out = tumpukan.pop()
print('data keluar :', out)
print('data sekarang :', tumpukan)

