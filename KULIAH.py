

"""
Jika variabel a sama dengan variabel b, cetaklah nilai dari variabel a, jika sama, maka 
cetaklah nilai dari variabel b.

"""

a = int(input("Masukkan niai a :"))
b = int(input("Masukkan nilai b :"))

if ( a == b):
    print("Nilai a sama dengan b, yaitu :", a)
else:
    print("Nilai a dan nilai b tidak memiliki kesamaan")

